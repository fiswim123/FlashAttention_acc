def run_multiple_tests(seeds, S=256, d=64):
    """Run multiple tests with different seeds."""
    print(f"\n{'='*60}")
    print(f"Running {len(seeds)} end-to-end tests")
    print(f"{'='*60}")

    results = []

    for idx, seed in enumerate(seeds):
        Q, K, V = generate_random_inputs(S, d, seed=seed)
        O_golden = flash_attention_tiled(Q, K, V, causal=True, S=S, d=d)

        results.append({
            'test_id': idx,
            'seed': seed,
            'S': S,
            'd': d,
            'Q': Q,
            'K': K,
            'V': V,
            'O_golden': O_golden,
        })

    # Save all test vectors
    save_test_vectors(results, "verify/test_vectors_all.txt")
    print(f"\nAll test vectors saved to verify/test_vectors_all.txt")

    # Generate summary
    print(f"\nGenerated {len(seeds)} test cases:")
    for r in results:
        print(f"  seed={r['seed']}: Q=[{r['Q'].min()},{r['Q'].max()}], "
              f"K=[{r['K'].min()},{r['K'].max()}], "
              f"V=[{r['V'].min()},{r['V'].max()}], "
              f"O=[{r['O_golden'].min()},{r['O_golden'].max()}]")

    return results


