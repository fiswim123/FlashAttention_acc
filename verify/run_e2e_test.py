def q88_to_hex(val):
    """Convert Q8.8 int16 to hex string."""
    return f"0x{val & 0xFFFF:04x}"


def print_matrix_summary(name, matrix, max_rows=3, max_cols=8):
    """Print summary of a matrix."""
    print(f"\n{name} shape={matrix.shape}, dtype={matrix.dtype}")
    print(f"  Range: [{matrix.min()}, {matrix.max()}]")
    print(f"  First {max_rows} rows (first {max_cols} cols):")
    for i in range(min(max_rows, matrix.shape[0])):
        row_str = ' '.join(f"{matrix[i,j]:6d}" for j in range(min(max_cols, matrix.shape[1])))
        print(f"    [{i}]: {row_str} ...")


def run_single_test(seed, S=256, d=64):
    """Run a single end-to-end test."""
    print(f"\n{'='*60}")
    print(f"Test with seed={seed}, S={S}, d={d}")
    print(f"{'='*60}")

    # Generate random inputs
    print("\n1. Generating random Q, K, V (Q8.8)...")
    Q, K, V = generate_random_inputs(S, d, seed=seed)
    print_matrix_summary("Q", Q)
    print_matrix_summary("K", K)
    print_matrix_summary("V", V)

    # Compute golden output
    print("\n2. Computing FP32 golden output (FlashAttention tiled)...")
    O_golden = flash_attention_tiled(Q, K, V, causal=True, S=S, d=d)
    print_matrix_summary("O_golden", O_golden)

    # Save test vectors
    test_vectors_file = f"verify/test_vectors_seed{seed}.txt"
    test_cases = [{
        'test_id': 0,
        'seed': seed,
        'S': S,
        'd': d,
        'Q': Q,
        'K': K,
        'V': V,
        'O_golden': O_golden,
    }]
    save_test_vectors(test_cases, test_vectors_file)
    print(f"\n3. Test vectors saved to {test_vectors_file}")

    # Generate memory initialization files
    print("\n4. Generating memory initialization files...")

    # Q memory (for DMA load)
    with open(f"verify/mem_Q_seed{seed}.hex", 'w') as f:
        for i in range(S):
            for j in range(d):
                f.write(f"{int(Q[i,j]) & 0xFFFF:04x}\n")
    print(f"   Q memory: verify/mem_Q_seed{seed}.hex")

    # K memory
    with open(f"verify/mem_K_seed{seed}.hex", 'w') as f:
        for i in range(S):
            for j in range(d):
                f.write(f"{int(K[i,j]) & 0xFFFF:04x}\n")
    print(f"   K memory: verify/mem_K_seed{seed}.hex")

    # V memory
    with open(f"verify/mem_V_seed{seed}.hex", 'w') as f:
        for i in range(S):
            for j in range(d):
                f.write(f"{int(V[i,j]) & 0xFFFF:04x}\n")
    print(f"   V memory: verify/mem_V_seed{seed}.hex")

    # O golden reference
    with open(f"verify/mem_O_golden_seed{seed}.hex", 'w') as f:
        for i in range(S):
            for j in range(d):
                f.write(f"{int(O_golden[i,j]) & 0xFFFF:04x}\n")
    print(f"   O golden: verify/mem_O_golden_seed{seed}.hex")

    # Generate Verilog testbench with embedded data
    print("\n5. Generating Verilog testbench...")
    generate_verilog_testbench(Q, K, V, O_golden, seed, S, d)

    return Q, K, V, O_golden


